export default function BottomNav() {
  const navigate = useNavigate();
  const location = useLocation();
  const userRole = localStorage.getItem("userRole");

  const tabs = [
    { id: "home", label: "Home", path: "/dashboard", Icon: Icons.Home },
    { id: "portfolio", label: "Portfolio", path: "/portfolio", Icon: Icons.Portfolio },
    { id: "network", label: "Network", path: "/network", Icon: Icons.Network },
    { id: "settings", label: "Settings", path: "/settings", Icon: Icons.Settings },
  ];

  // Add Admin tab only if the role is 'admin'
  if (userRole === "admin") {
    tabs.push({ id: "admin", label: "Admin", path: "/admin", Icon: Icons.Admin });
  }

  return (
    <nav className="glass-tabs">
      {tabs.map((tab) => (
        <div 
          key={tab.id} 
          className={`tab-btn ${location.pathname === tab.path ? "active" : ""}`}
          onClick={() => navigate(tab.path)}
          style={tab.id === 'admin' ? { color: '#ff4444' } : {}}
        >
          <tab.Icon />
          <span>{tab.label}</span>
        </div>
      ))}
    </nav>
  );
}